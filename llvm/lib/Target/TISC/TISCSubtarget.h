#ifndef LLVM_LIB_TARGET_TISC_TISCSUBTARGET_H
#define LLVM_LIB_TARGET_TISC_TISCSUBTARGET_H

#include "TISCFrameLowering.h"
#include "TISCISelLowering.h"
#include "TISCInstrInfo.h"
#include "TISCRegisterInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "TISCGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class TISCSubtarget : public TISCGenSubtargetInfo {
public:
  enum HWMultEnum {
    NoHWMult, HWMult16, HWMult32, HWMultF5
  };

private:
  virtual void anchor();
  bool ExtendedInsts = false;
  HWMultEnum HWMultMode = NoHWMult;
  TISCInstrInfo InstrInfo;
  TISCTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
  TISCFrameLowering FrameLowering;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  TISCSubtarget(const Triple &TT, const std::string &CPU,
                  const std::string &FS, const TargetMachine &TM);

  TISCSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  bool hasHWMult16() const { return HWMultMode == HWMult16; }
  bool hasHWMult32() const { return HWMultMode == HWMult32; }
  bool hasHWMultF5() const { return HWMultMode == HWMultF5; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const TISCInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const TISCRegisterInfo *getRegisterInfo() const override {
    return &getInstrInfo()->getRegisterInfo();
  }

  const TISCTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};
} // End llvm namespace

#endif  // LLVM_TARGET_TISC_SUBTARGET_H
